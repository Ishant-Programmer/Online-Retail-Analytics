/*=====================================================
SECTION 1: DATA EXPLOARATION
=====================================================*/

-- Query 1: Total Records 
-- Count the total number of transactional records.
SELECT COUNT(*) AS Total_Records FROM online_retail;

-- Query 2 : Unique Customers
-- Count the total number of unique customers.
SELECT COUNT(DISTINCT CustomerID) AS Unique_Customers FROM online_retail;

-- Query 3 : Unique Products
-- Count the total number of unique products.
SELECT COUNT(DISTINCT StockCode) AS Unique_Products FROM online_retail; -- 

-- Query 4 : Number of Countries
-- Count the number of countries where sales were made.
SELECT COUNT(DISTINCT Country) AS Countries FROM online_retail;

-- Query 5 : List of Countries
-- Display all countries present in the dataset.
SELECT DISTINCT Country FROM online_retail ORDER BY Country;

-- Query 6 : Date Range
-- Find the first and last transaction dates.
SELECT MIN(InvoiceDate) AS First_Order, MAX(InvoiceDate) AS Last_Order FROM online_retail;

-- Query 7 : Total Revenue
-- Calculate the total revenue generated.
SELECT SUM(Revenue) AS Total_Revenue FROM online_retail;

-- Query 8 : Average Revenue 
-- Calculate the average revenue generated per transaction line.
SELECT AVG(Revenue) AS Average_Revenue FROM online_retail;

-- Query 9 : Missing Customer IDs
-- Count transaction records with missing Customer IDs.
SELECT COUNT(*) AS Missing_CustomerID FROM online_retail WHERE CustomerID IS NULL OR CustomerID='';

/*=====================================================
Section 2: Sales Analysis
=====================================================*/

-- Query 1 : Monthly Revenue Trend
-- Calculate monthly revenue to analyze sales trends over time.
SELECT Year, Month, Month_Name, SUM(Revenue) AS Monthly_Revenue
FROM online_retail 
GROUP BY Year, Month, Month_Name
ORDER BY Year, Month;

-- Query 2 : Monthly Order Trend
-- Calculate the number of unique orders placed each month.
SELECT Year, Month, Month_Name, COUNT(DISTINCT(Invoice)) AS Total_Orders
FROM online_retail
GROUP BY Year, Month, Month_Name
ORDER BY Year, Month;

-- Query 3 : Top Revenue Products
-- Display the top 10 products generating the highest revenue.
SELECT Description, SUM(Revenue) AS Total_Revenue
FROM online_retail
GROUP BY Description 
ORDER BY Total_Revenue DESC 
LIMIT 10;

-- Query 4 : Top Selling Products
-- Display the top 10 best-selling products by quantity sold.
SELECT StockCode, Description, SUM(Quantity) AS Quantity_Sold
FROM online_retail 
GROUP BY StockCode, Description
ORDER BY Quantity_Sold DESC
LIMIT 10;

-- Query 5 : Average Order Value (AOV)
-- Calculate the average revenue generated per order.
SELECT SUM(Revenue)/COUNT(DISTINCT Invoice) AS Average_Order_Value
FROM online_retail;

-- Query 6 : Top Customers by Revenue
-- Display the top 10 customers generating the highest revenue.
SELECT CustomerID, SUM(Revenue) AS Total_Revenue
FROM online_retail
WHERE CustomerID <> ''
GROUP BY CustomerID
ORDER BY Total_Revenue DESC
LIMIT 10;

-- Query 7 : Average Revenue by Country
-- Calculate the average revenue generated by each country.
SELECT Country, AVG(Revenue) AS Average_Revenue
FROM online_retail
GROUP BY Country 
ORDER BY Average_Revenue DESC;

-- Query 8 : Top Countries by Revenue
-- Display countries ranked by total revenue generated.
SELECT Country, SUM(Revenue) AS Total_Revenue
FROM online_retail
GROUP BY Country
ORDER BY Total_Revenue DESC 
LIMIT 10;

-- Query 9 : Monthly Average Order Value
-- Calculate the average order value for each month.
SELECT Year, Month, Month_Name, SUM(Revenue)/COUNT(DISTINCT Invoice) AS Average_Order_Value
FROM online_retail
GROUP BY Year, Month, Month_Name
ORDER BY Year, Month;

