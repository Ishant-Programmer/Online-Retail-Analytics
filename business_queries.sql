-- Create the table to store the cleaned online retail dataset.
CREATE TABLE online_retail (
    Invoice VARCHAR(20),
    StockCode VARCHAR(30),
    Description TEXT,
    Quantity INT,
    InvoiceDate DATETIME,
    Price DOUBLE,
    CustomerID VARCHAR(20),
    Country VARCHAR(100),
    Revenue DOUBLE,
    Month INT,
    Month_Name VARCHAR(20),
    Year INT
);

-- Import the cleaned dataset into the table.
LOAD DATA LOCAL INFILE
"C:/Users/ishan/Desktop/Online Retail Analytics/data/cleaned/online_retail_cleaned.csv"
INTO TABLE online_retail
FIELDS TERMINATED BY ','
ENCLOSED BY '"'
LINES TERMINATED BY '\r\n'
