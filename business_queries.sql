-- Query 2 : Customer Performance Report
/*Generate a customer performance report showing total orders,
total revenue, and average order value for each customer.*/
SELECT CustomerID, COUNT(DISTINCT Invoice) AS Total_Orders, SUM(Revenue) AS Total_Revenue,
SUM(Revenue)/COUNT(DISTINCT Invoice) AS Average_Order_Value
FROM online_retail
