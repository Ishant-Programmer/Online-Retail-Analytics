-- Query 4 : VIP Customers
-- Display customers who have generated more than 10,000 in total revenue.
SELECT CustomerID, COUNT(DISTINCT Invoice) AS Total_Orders, SUM(Revenue) AS Total_Revenue
FROM online_retail
WHERE CustomerID <> ''
GROUP BY CustomerID
HAVING SUM(Revenue) > 10000
ORDER BY Total_Revenue DESC;

-- Query 5 : Customer Segmentation (Using CASE)
-- Categorize customers into High, Medium, and Low value segments based on total revenue.
SELECT CustomerID, SUM(Revenue) AS Total_Revenue,
CASE 
	WHEN SUM(Revenue) >= 10000 THEN 'High Value'
    WHEN SUM(Revenue) >= 5000 THEN 'Medium Value'
    ELSE 'Low Value'
END AS Customer_Segment 
FROM online_retail
WHERE CustomerID <> ''
GROUP BY CustomerID
ORDER BY Total_Revenue DESC;

-- Query 6 : Customer Performance Dashboard
/*Generate a customer performance dashboard showing total orders, total revenue, 
average order value, and average items per order for each customer.*/
SELECT CustomerID, COUNT(DISTINCT Invoice) AS Total_Orders,
SUM(Revenue) AS Total_Revenue, SUM(Revenue)/COUNT(DISTINCT Invoice) AS Average_Order_Value,
SUM(Quantity)/COUNT(DISTINCT Invoice) AS Average_Items_Per_Order
FROM online_retail
WHERE CustomerID <> ''
GROUP BY CustomerID
ORDER BY Total_Revenue DESC;

