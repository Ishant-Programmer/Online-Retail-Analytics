/*=====================================================
Section 6: Advanced SQL
=====================================================*/

-- Query 1 : Customers Above Average Revenue 
-- Display customers whose total revenue is above the average customer revenue.
WITH Customer_Summary AS 
(
	SELECT CustomerID, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    WHERE CustomerID <> ''
    GROUP BY CustomerID
)
SELECT CustomerID, Total_Revenue
FROM Customer_Summary
WHERE Total_Revenue> (SELECT AVG(Total_Revenue) FROM Customer_Summary)
ORDER BY Total_Revenue DESC;

-- Query 2 : Customer Ranking Using ROW_NUMBER()
-- Assign a unique rank to each customer based on total revenue.
WITH Customer_Summary AS 
(
	SELECT CustomerID, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    WHERE CustomerID <> ''
    GROUP BY CustomerID
)
SELECT ROW_NUMBER() OVER (ORDER BY Total_Revenue DESC) AS Customer_Rank, CustomerID, Total_Revenue
FROM Customer_Summary;

-- Query 3 : Customer Ranking Using RANK()
-- Assign the same rank to customers with equal revenue. The next rank is skipped when a tie occurs.
WITH Customer_Summary AS 
(
	SELECT CustomerID, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    WHERE CustomerID <> ''
    GROUP BY CustomerID
)
SELECT RANK() OVER (ORDER BY Total_Revenue DESC) AS Customer_Rank, CustomerID, Total_Revenue
FROM Customer_Summary;

-- Query 4 : Customer Ranking Using DENSE_RANK()
-- Assign the same rank to customers with equal revenue without skipping rank numbers.
WITH Customer_Summary AS
(
	SELECT CustomerID, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    WHERE CustomerID <> ''
    GROUP BY CustomerID
)
SELECT DENSE_RANK() OVER (ORDER BY Total_Revenue DESC) AS Customer_Rank, CustomerID, Total_Revenue
FROM Customer_Summary;

-- Query 5 : Customer Ranking Within Each Country
-- Rank customers by total revenue within each country.
WITH Customer_Summary AS
(
	SELECT Country, CustomerID, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    WHERE CustomerID <> ''
    GROUP BY Country, CustomerID
)
SELECT Country, CustomerID, Total_Revenue, 
DENSE_RANK() OVER (PARTITION BY Country ORDER BY Total_Revenue DESC) AS Customer_Rank 
FROM Customer_Summary
ORDER BY Country, Customer_Rank;

-- Query 6 : Previous Month Revenue Using LAG()
-- Display monthly revenue along with the previous month's revenue.
WITH Monthly_Revenue AS 
(
	SELECT Year, Month, Month_Name, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    GROUP BY Year, Month, Month_Name
)
SELECT Year, Month, Month_Name, Total_Revenue,
LAG(Total_Revenue) OVER (ORDER BY Year, Month) AS Previous_Month_Revenue
FROM Monthly_Revenue;

-- Query 7 : Month-over-Month Revenue Growth
-- Calculate the month-over-month revenue growth percentage.
WITH Monthly_Revenue AS 
(
	SELECT Year, Month, Month_Name, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    GROUP BY Year, Month, Month_Name
),
Revenue_Comparision AS 
(
	SELECT Year, Month, Month_Name, Total_Revenue,
    LAG(Total_Revenue) OVER (ORDER BY Year, Month) AS Previous_Month_Revenue
    FROM Monthly_Revenue
)
SELECT Year, Month, Month_Name, Total_Revenue, Previous_Month_Revenue,
((Total_Revenue - Previous_Month_Revenue)/Previous_Month_Revenue) *100 AS MoM_Growth_Percentage
FROM Revenue_Comparision;

-- Query 8 : Running Total Revenue
-- Calculate the cumulative revenue over time.
WITH Monthly_Revenue AS
(
	SELECT Year, Month, Month_Name, SUM(Revenue) AS Total_Revenue
    FROM online_retail 
    GROUP BY Year, Month, Month_Name
)
SELECT Year, Month, Month_Name, Total_Revenue,
SUM(Total_Revenue) OVER (ORDER BY Year, Month) AS Running_Total_Revenue
FROM Monthly_Revenue;

-- Query 9 : Top Product in Each Country
-- Display the highest revenue-generating product in each country.
WITH Product_Revenue AS 
(
	SELECT Country, StockCode, Description, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    GROUP BY Country, StockCode, Description
),
Ranked_Products AS 
(
	SELECT Country, StockCode, Description, Total_Revenue,
    ROW_NUMBER() OVER (PARTITION BY Country ORDER BY Total_Revenue DESC) AS Product_Rank
    FROM Product_Revenue
)
SELECT Country, StockCode, Description, Total_Revenue
FROM Ranked_Products
WHERE Product_Rank=1
ORDER BY Country;

-- Query 10 : Top Customer in Each Country
-- Display the highest revenue-generating customer in each country.
WITH Customer_Revenue AS 
(
	SELECT Country, CustomerID, SUM(Revenue) AS Total_Revenue
    FROM online_retail
    WHERE CustomerID <> ''
    GROUP BY Country, CustomerID 
),
Ranked_Customers AS 
(
	SELECT Country, CustomerID, Total_Revenue, 
    ROW_NUMBER() OVER (PARTITION BY Country ORDER BY Total_Revenue DESC) AS Customer_Rank
    FROM Customer_Revenue
)
SELECT Country, CustomerID, Total_Revenue 
FROM Ranked_Customers
WHERE Customer_Rank=1 
ORDER BY Country;

